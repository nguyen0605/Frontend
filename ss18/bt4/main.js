let n = Number(prompt("Nhập vào 1 số (0->9):"));

switch(n){
    case 1: alert("Số một");break;
    case 2: alert("Số hai");break;
    case 3: alert("Số ba");break;
    case 4: alert("Số bốn");break;
    case 5: alert("Số năm");break;
    case 6: alert("Số sáu");break;
    case 7: alert("Số bảy");break;
    case 8: alert("Số tám");break;
    case 9: alert("Số chín");break;
    case 0: alert("Số không");break;
    default: alert("Số nhập vào không hợp lệ! (0->9)"); break;
}