// Sử dụng hàm Math.abs()
document.writeln("Math.abs(-5.5) = " + Math.abs(-5.5) + "<br>"); 
// Giải thích: Trả về giá trị tuyệt đối của -5.5 là 5.5

document.writeln("Math.abs(10) = " + Math.abs(10) + "<br>"); 
// Giải thích: Trả về giá trị tuyệt đối của 10, kết quả vẫn là 10

// Sử dụng hàm Math.ceil()
document.writeln("Math.ceil(4.4) = " + Math.ceil(4.4) + "<br>"); 
// Giải thích: Làm tròn lên 4.4 thành 5

document.writeln("Math.ceil(5.8) = " + Math.ceil(5.8) + "<br>"); 
// Giải thích: Làm tròn lên 5.8 thành 6

// Sử dụng hàm Math.floor()
document.writeln("Math.floor(5.7) = " + Math.floor(5.7) + "<br>"); 
// Giải thích: Làm tròn xuống 5.7 thành 5

document.writeln("Math.floor(6.1) = " + Math.floor(6.1) + "<br>"); 
// Giải thích: Làm tròn xuống 6.1 thành 6

// Sử dụng hàm Math.round()
document.writeln("Math.round(10.1) = " + Math.round(10.1) + "<br>"); 
// Giải thích: Làm tròn 10.1 về số nguyên gần nhất là 10

document.writeln("Math.round(20.6) = " + Math.round(20.6) + "<br>"); 
// Giải thích: Làm tròn 20.6 về số nguyên gần nhất là 21
